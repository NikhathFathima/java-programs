package conditional_blocks;

public class If_else_conditional_statement {

	public static void main(String[] args) {
		int no1=90;
		int no2=10;
		
		if(no1>no2)//(4<1) else block will execute
		{
			System.out.println("no1 is greater");//under any circumstances only if or else only 1 block will execute
		}
		else
		{
			System.out.println("no2 is greater");
		}

	}

}

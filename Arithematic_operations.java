package java__basics_prgms;

public class Arithematic_operations {
	
	static int a=10;//global variable
	static int b=20;

	public static void main(String[] args) {
		
		
		int sum=a+b;
		System.out.println("Addition of 2 numbers = "+ sum);
		
		
		int subtraction=b-a;
		System.out.println("subtraction of 2 numbers = "+ subtraction);
		
		
		int mul=a*b;
		System.out.println("multiplication of 2 numbers = "+ mul);
		
		int div=a/b;
		System.out.println("division of 2 numbers = "+ div);
		
		int mod=a%b;
		System.out.println("modulas od 2 numbers ="+ mod);
		

	}

}

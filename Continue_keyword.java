package continue_keyword;

public class Continue_keyword {

	public static void main(String[] args) {
		int i;
		for(i=1;i<=5;i++) {
			
			if(i==3)
			{
				continue;//whichever we want to skip their we use continue keyword
			}

			if(i==4)
			{
				continue;//whichever we want to skip their we use continue keyword
			}
			System.out.println(i);
		}

	}

}
